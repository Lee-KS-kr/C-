#include <iostream>

using namespace std;

int main() {
	short a = -7000;
	long b = -37720;
	int c = 783423896;

	unsigned short d = 4235;
	unsigned long e = 2428;
	unsigned int f = 512164;

	cout << "a = " << a << endl;
	cout << "b = " << b << endl;
	cout << "c = " << c << endl;
	cout << "d = " << d << endl;
	cout << "e = " << e << endl;
	cout << "f = " << f << endl;

	float g = 25.284228f;
	double h = 5722.5554128f;

	cout << "g = " << g << endl;
	cout << "h = " << h << endl;

	char A = 48;
	char AA = 'A';
	cout << "A = " << A << endl;
	cout << "AA = " << AA << endl;

	bool TRUE = true;
	bool FALSE = false;

	cout << "TRUE = " << TRUE << endl;
	cout << "FALSE = " << FALSE << endl;

	return 0;
}